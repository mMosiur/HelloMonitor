namespace HelloMonitor {
    /// <summary>
    /// Styles of page animations for apearing/disapearing
    /// </summary>
    public enum PageAnimation {
        /// <summary>
        /// No animation takes place
        /// </summary>
        None = 0,
        /// <summary>
        /// Page slides in and page fades in from the left
        /// </summary>
        SlideAndFadeInFromLeft = 1,
        /// <summary>
        /// Page fades out
        /// </summary>
        FadeOut = 2,
    }
}
